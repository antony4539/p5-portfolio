function setup() {
  createCanvas(600, 400);
  noLoop(); 
}

function draw() {
  background(240, 248, 255);

  stroke(0);
  strokeWeight(2);
  
  // 회색 긴팔
  fill(160);
  rect(150, 320, 50, 100, 10);
  rect(400, 320, 50, 100, 10);

  // 남색 반팔
  fill(0, 0, 128);
  rect(130, 300, 80, 60, 15);
  rect(390, 300, 80, 60, 15);
  rect(190, 300, 220, 150, 20);

  // 목
  fill(255, 218, 185);
  rect(280, 250, 40, 60);

  // 귀
  fill(255, 218, 185);
  ellipse(215, 180, 20, 40);
  ellipse(385, 180, 20, 40);

  // 얼굴
  ellipse(300, 180, 160, 200);

  // 삐죽삐죽한 머리카락 (비니 아래로 튀어나옴)
  fill(50);
  noStroke();
  
  triangle(240, 130, 255, 160, 270, 130);
  triangle(265, 130, 285, 165, 305, 130);
  triangle(295, 130, 315, 160, 335, 130);
  triangle(330, 130, 345, 155, 360, 130);


  // 비니 (모자 본체와 챙을 제거하고, 부드러운 형태로 덮기)
  fill(100, 100, 150); // 비니 몸체 색상
  stroke(0);
  strokeWeight(2);
  
  // 부드럽게 둥근 비니 윗부분
  arc(300, 135, 160, 110, PI, TWO_PI, CHORD);
  
  // 비니의 아래 접힌 부분 (부드러운 타원)
  ellipse(300, 135, 160, 25);
  
  // 비니 질감 (세로줄로 니트 느낌 표현)
  strokeWeight(1);
  line(260, 125, 260, 145);
  line(280, 125, 280, 145);
  line(300, 125, 300, 145);
  line(320, 125, 320, 145);
  line(340, 125, 340, 145);

  // 목걸이 추가 (회색 옷 깃 주변에 은색 체인과 펜던트)
  fill(200); // 은색
  stroke(100);
  strokeWeight(2);
  
  // 은색 체인 (목 아래로 흐르는 간단한 아크)
  arc(300, 308, 40, 15, HALF_PI, PI);
  arc(300, 308, 40, 15, 0, HALF_PI);
  
  // 은색 펜던트 (간단한 원판)
  fill(200);
  noStroke();
  ellipse(300, 315, 12, 12);

  // 눈썹
  stroke(0);
  strokeWeight(4);
  line(250, 165, 280, 170);
  line(350, 165, 320, 170);

  // 눈
  strokeWeight(2);
  fill(0);
  ellipse(265, 190, 12, 12);
  ellipse(335, 190, 12, 12);

  // 코
  noFill();
  arc(300, 220, 20, 20, 0, PI);

  // 입
  fill(255, 100, 100);
  arc(300, 255, 50, 30, 0, PI, CHORD);
}